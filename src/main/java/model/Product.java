package model;

public class Product {
	int _id;
	String _name;
	int _typeId;
	double _weight;
	double _originPrice;
	int _imageId;
	int _saleOffId;
	
	
	public int get_id() {
		return _id;
	}
	public void set_id(int _id) {
		this._id = _id;
	}
	public String get_name() {
		return _name;
	}
	public void set_name(String _name) {
		this._name = _name;
	}
	public int get_typeId() {
		return _typeId;
	}
	public void set_typeId(int _typeId) {
		this._typeId = _typeId;
	}
	public double get_weight() {
		return _weight;
	}
	public void set_weight(double _weight) {
		this._weight = _weight;
	}
	public double get_originPrice() {
		return _originPrice;
	}
	public void set_originPrice(double _originPrice) {
		this._originPrice = _originPrice;
	}
	public int get_imageId() {
		return _imageId;
	}
	public void set_imageId(int _imageId) {
		this._imageId = _imageId;
	}
	public int get_saleOffId() {
		return _saleOffId;
	}
	public void set_saleOffId(int _saleOffId) {
		this._saleOffId = _saleOffId;
	}
	
	
	

}
